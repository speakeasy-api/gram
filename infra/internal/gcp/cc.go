// pubsubAPI is the Google Cloud service that must be enabled for the generated
// topics and subscriptions to reconcile.
const pubsubAPI = "pubsub.googleapis.com"

const ccValuesHeader = "# Code generated by cc-gen. DO NOT EDIT.\n"

type ConfigConnectorPubSub struct {
	logger      *slog.Logger
	outPath     string
	descriptors []byte
	protoRoot   string
}

func NewCCPubSub(logger *slog.Logger, outPath string, descriptors []byte, protoRoot string) *ConfigConnectorPubSub {
	return &ConfigConnectorPubSub{
		logger:      logger,
		outPath:     outPath,
		descriptors: descriptors,
		protoRoot:   protoRoot,
	}
}

// Generate discovers the Pub/Sub topology declared in the embedded protobuf
// descriptors and writes it as a Helm values document. The Config Connector
// resources themselves are rendered by a generic template in the deployment
// chart that ranges over this topology, so per-resource deployment metadata
// (project, namespace, deletion/prune policy) lives in the chart rather than
// here.
func (c *ConfigConnectorPubSub) Generate(ctx context.Context) error {
	if len(c.descriptors) == 0 {
		return fmt.Errorf("descriptor set is empty: refusing to generate an empty pubsub topology")
	}

	desiredTopics, desiredSubs, err := DiscoverPubSub(c.descriptors)
	if err != nil {
		return fmt.Errorf("discover pubsub topology: %w", err)
	}

	desiredSchemas, err := DiscoverSchemas(ctx, c.descriptors, c.protoRoot)
	if err != nil {
		return fmt.Errorf("discover pubsub schemas: %w", err)
	}

	if err := c.writeValues(ctx, buildPubSubValues(ctx, c.logger, desiredTopics, desiredSubs, desiredSchemas)); err != nil {
		return fmt.Errorf("write pubsub values: %w", err)
	}

	if err := c.writeBigQueryDDL(ctx, c.logger, desiredTopics); err != nil {
		return fmt.Errorf("write bigquery ddl: %w", err)
	}

	return nil
}

func (c *ConfigConnectorPubSub) writeValues(ctx context.Context, doc pubSubValuesDocument) error {
	if err := os.MkdirAll(filepath.Dir(c.outPath), 0o755); err != nil {
		return fmt.Errorf("create output directory: %w", err)
	}

	out, err := yaml.Marshal(doc)
	if err != nil {
		return fmt.Errorf("marshal pubsub values: %w", err)
	}

	var buf bytes.Buffer
	buf.WriteString(ccValuesHeader)
	buf.Write(out)

	// Write atomically: stage the full document in a temp file alongside the
	// target, then rename it into place. A rename on the same filesystem is
	// atomic, so an interruption leaves either the previous file or the new
	// one intact, never a truncated partial write.
	tmp, err := os.CreateTemp(filepath.Dir(c.outPath), filepath.Base(c.outPath)+".tmp-*")
	if err != nil {
		return fmt.Errorf("create temp file: %w", err)
	}
	tmpPath := tmp.Name()
	// Best-effort cleanup: harmless no-ops once the file is renamed away.
	defer func() {
		_ = tmp.Close()
		_ = os.Remove(tmpPath)
	}()

	if _, err := tmp.Write(buf.Bytes()); err != nil {
		return fmt.Errorf("write temp file: %w", err)
	}
	if err := tmp.Chmod(0o644); err != nil {
		return fmt.Errorf("chmod temp file: %w", err)
	}
	if err := tmp.Sync(); err != nil {
		return fmt.Errorf("sync temp file: %w", err)
	}
	if err := tmp.Close(); err != nil {
		return fmt.Errorf("close temp file: %w", err)
	}

	if err := os.Rename(tmpPath, c.outPath); err != nil {
		return fmt.Errorf("rename %s to %s: %w", tmpPath, c.outPath, err)
	}

	c.logger.InfoContext(ctx, "pubsub values written", attr.SlogFilePath(c.outPath))
	return nil
}

func (c *ConfigConnectorPubSub) writeBigQueryDDL(ctx context.Context, logger *slog.Logger, desiredTopics []DesiredTopic) error {
	sorted := slices.SortedStableFunc(slices.Values(desiredTopics), func(a, b DesiredTopic) int {
		return cmp.Compare(a.ProtoMessage, b.ProtoMessage)
	})

	builder := &strings.Builder{}

	for _, topic := range sorted {
		logger := logger.With(attr.SlogTopicProtoName(topic.ProtoMessage))

		builder.Reset()
		fmt.Fprint(builder, "-- Code generated by cc-gen. DO NOT EDIT.\n\n")
		ddl, err := appendBigQueryDDL(ctx, logger, topic)
		if err != nil {
			return fmt.Errorf("generate bigquery ddl for %s: %w", topic.ProtoMessage, err)
		}
		if len(ddl) == 0 {
			continue
		}

		builder.Write(ddl)
		builder.WriteString("\n\n")

		result := builder.String()
		if len(result) == 0 {
			logger.InfoContext(ctx, "no bigquery ddl generated")
			return nil
		}

		result = result[:len(result)-1]

		outDir := filepath.Join(filepath.Dir(c.outPath), "bq")
		if err := os.MkdirAll(outDir, 0o755); err != nil {
			return fmt.Errorf("create bigquery ddl directory: %w", err)
		}

		fileName := strings.ToLower(strings.Join(strings.Split(topic.ProtoMessage, "."), "_")) + ".sql"
		outPath := filepath.Join(outDir, fileName)
		if err := os.WriteFile(outPath, []byte(result), 0o644); err != nil {
			return fmt.Errorf("write bigquery ddl: %w", err)
		}

		logger.InfoContext(ctx, "bigquery ddl written", attr.SlogFilePath(outPath))
	}

	return nil
}

func appendBigQueryDDL(_ context.Context, _ *slog.Logger, desiredTopics DesiredTopic) ([]byte, error) {
	if desiredTopics.ProtoDescriptor == nil || desiredTopics.ProtoDescriptor.Fields().Len() == 0 {
		return []byte{}, nil
	}

	parts := strings.Split(desiredTopics.ProtoMessage, ".")
	if len(parts) < 2 {
		return nil, fmt.Errorf("invalid proto message name: %s", desiredTopics.ProtoMessage)
	}

	tableName := strings.ToLower(strings.Join(parts[1:], "_"))

	type column struct{ name, kind string }
	var columns []column
	fi := &fieldIter{}
	for k, v := range fi.bqFields(desiredTopics.ProtoDescriptor) {
		columns = append(columns, column{name: k, kind: v})
	}
	if fi.err != nil {
		return nil, fmt.Errorf("generate bigquery ddl for %s: %w", desiredTopics.ProtoMessage, fi.err)
	}
	if len(columns) == 0 {
		return []byte{}, nil
	}

	builder := &bytes.Buffer{}

	// Seed the table with a throwaway stub column so CREATE never has to know the
	// real schema; the columns are reconciled by the ALTER below, which doubles
	// as schema evolution for already-existing tables. Each CREATE/ALTER is a
	// table-metadata update operation, and BigQuery caps those at 5 per 10
	// seconds per table, so every column is added — and the stub dropped — in a
	// single ALTER rather than one statement apiece.
	fmt.Fprintf(builder, "CREATE TABLE IF NOT EXISTS %s (__stub INT64);", tableName)

	fmt.Fprintf(builder, "\nALTER TABLE %s", tableName)
	for _, c := range columns {
		fmt.Fprintf(builder, "\n  ADD COLUMN IF NOT EXISTS %s %s,", c.name, c.kind)
	}
	builder.WriteString("\n  DROP COLUMN IF EXISTS __stub;")

	return builder.Bytes(), nil
}

// durationToGCPString formats a duration as an integer-seconds string (e.g. "86400s")
// as expected by Config Connector duration fields.
func durationToGCPString(d time.Duration) string {
	return fmt.Sprintf("%ds", int64(d.Seconds()))
}

type fieldIter struct {
	err error
}

func (fi *fieldIter) bqFields(msg protoreflect.MessageDescriptor) iter.Seq2[string, string] {
	return func(yield func(string, string) bool) {
		if msg == nil {
			return
		}

		fields := msg.Fields()
		for i := 0; i < fields.Len(); i++ {
			f := fields.Get(i)

			bqkind := ""
			switch f.Kind() {
			case protoreflect.BoolKind:
				bqkind = "BOOL"
			case protoreflect.EnumKind:
				bqkind = "STRING"
			case protoreflect.Int32Kind, protoreflect.Sint32Kind, protoreflect.Uint32Kind, protoreflect.Int64Kind, protoreflect.Sint64Kind:
				bqkind = "INT64"
			case protoreflect.Uint64Kind, protoreflect.Sfixed32Kind, protoreflect.Fixed32Kind:
				bqkind = "NUMERIC"
			case protoreflect.Sfixed64Kind, protoreflect.Fixed64Kind:
				bqkind = "BIGNUMERIC"
			case protoreflect.FloatKind, protoreflect.DoubleKind:
				bqkind = "FLOAT64"
			case protoreflect.StringKind:
				bqkind = "STRING"
			case protoreflect.BytesKind:
				bqkind = "BYTES"
			case protoreflect.MessageKind:
				bqkind = "JSON"
			default:
				fi.err = errors.Join(fi.err, fmt.Errorf("%s: unsupported field kind: %s", f.Name(), f.Kind()))
			}

			if f.IsMap() {
				bqkind = fmt.Sprintf("ARRAY<STRUCT<key STRING, value %s>>", bqkind)
			}
			if f.IsList() {
				bqkind = fmt.Sprintf("ARRAY<%s>", bqkind)
			}

			if !yield(string(f.Name()), bqkind) {
				return
			}
		}
	}
}
