export type PolicyAction = "flag" | "block";

export type CheckScope =
  | "user_messages"
  | "system_messages"
  | "llm_responses"
  | "tool_arguments"
  | "tool_responses";

export type RuleCategory =
  | "secrets"
  | "financial"
  | "pii"
  | "government_ids"
  | "healthcare"
  | "custom";

export type DetectionRule = {
  id: string;
  description: string;
  source: "gitleaks" | "presidio";
};

export type McpServerScope = {
  slug: string;
  allTools: boolean;
  selectedTools: string[];
};

export type McpScope =
  | { mode: "all" }
  | { mode: "selected"; servers: McpServerScope[] };

export type DlpPolicy = {
  id: string;
  name: string;
  enabled: boolean;
  ruleCategories: RuleCategory[];
  selectedRules: string[];
  action: PolicyAction;
  scopes: CheckScope[];
  mcpScope: McpScope;
  createdAt: Date;
  updatedAt: Date;
};

