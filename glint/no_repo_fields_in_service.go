package glint

import (
	"go/ast"
	"go/parser"
	"go/token"
	"go/types"
	"strings"

	"golang.org/x/tools/go/analysis"
)

const (
	noRepoFieldsInServiceAnalyzer = "norepofieldsinservice"
	noRepoFieldsInServiceDoc      = "checks that annotated services do not store sqlc-generated *Queries types as fields"
)

type noRepoFieldsInServiceSettings struct {
	Disabled bool `json:"disabled"`
}

func newNoRepoFieldsInServiceAnalyzer(rule noRepoFieldsInServiceSettings) *analysis.Analyzer {
	return &analysis.Analyzer{
		Name: noRepoFieldsInServiceAnalyzer,
		Doc:  noRepoFieldsInServiceDoc,
		Run: func(pass *analysis.Pass) (any, error) {
			annotated := findAnnotatedStructs(pass)
			if len(annotated) == 0 {
				return nil, nil
			}

			cache := map[string]bool{}
			for _, s := range annotated {
				for _, field := range s.structType.Fields.List {
					if len(field.Names) == 0 {
						continue
					}

					fieldType := pass.TypesInfo.TypeOf(field.Type)
					if fieldType == nil {
						continue
					}

					ptr, ok := fieldType.(*types.Pointer)
					if !ok {
						continue
					}

					named, ok := ptr.Elem().(*types.Named)
					if !ok {
						continue
					}

					if !isSqlcGenerated(pass.Fset, named.Obj(), cache) {
						continue
					}

					pass.ReportRangef(field, "field %q in %s has type %s which is sqlc-generated; services should use *pgxpool.Pool and create repo instances in methods",
						field.Names[0].Name, s.name, fieldType)
				}
			}

			return nil, nil
		},
	}
}

// isSqlcGenerated checks whether the given type was defined in a file generated by sqlc.
// Results are cached by filename.
//
// NOTE: this parses the source file of the type declaration to check for the generated-code
// header comment. If more checks need cross-package type metadata in the future, consider
// migrating to the analysis fact system: a helper analyzer exports ObjectFacts on types
// defined in generated files, and downstream analyzers query via pass.ImportObjectFact.
func isSqlcGenerated(fset *token.FileSet, obj *types.TypeName, cache map[string]bool) bool {
	pos := fset.Position(obj.Pos())
	if pos.Filename == "" {
		return false
	}

	if result, ok := cache[pos.Filename]; ok {
		return result
	}

	result := false
	f, err := parser.ParseFile(token.NewFileSet(), pos.Filename, nil, parser.ParseComments|parser.PackageClauseOnly)
	if err == nil && ast.IsGenerated(f) {
		for _, cg := range f.Comments {
			if strings.Contains(cg.Text(), "sqlc") {
				result = true
				break
			}
		}
	}

	cache[pos.Filename] = result
	return result
}
