-- Create "global_roles" table
CREATE TABLE "global_roles" (
  "id" uuid NOT NULL DEFAULT generate_uuidv7(),
  "workos_slug" text NOT NULL,
  "workos_name" text NOT NULL,
  "workos_description" text NULL,
  "workos_created_at" timestamptz NOT NULL,
  "workos_updated_at" timestamptz NOT NULL,
  "workos_deleted_at" timestamptz NULL,
  "workos_last_event_id" text NULL,
  "created_at" timestamptz NOT NULL DEFAULT clock_timestamp(),
  "updated_at" timestamptz NOT NULL DEFAULT clock_timestamp(),
  "deleted_at" timestamptz NULL,
  "deleted" boolean NOT NULL GENERATED ALWAYS AS (deleted_at IS NOT NULL) STORED,
  PRIMARY KEY ("id")
);
-- Create "workos_organization_syncs" table
CREATE TABLE "workos_organization_syncs" (
  "id" bigint NOT NULL GENERATED BY DEFAULT AS IDENTITY,
  "workos_organization_id" text NOT NULL,
  "last_event_id" text NOT NULL,
  "created_at" timestamptz NOT NULL DEFAULT clock_timestamp(),
  PRIMARY KEY ("id")
);
-- Create index "workos_organization_syncs_workos_organization_id_key" to table: "workos_organization_syncs"
CREATE UNIQUE INDEX "workos_organization_syncs_workos_organization_id_key" ON "workos_organization_syncs" ("workos_organization_id");
-- Create "workos_user_syncs" table
CREATE TABLE "workos_user_syncs" (
  "id" bigint NOT NULL GENERATED BY DEFAULT AS IDENTITY,
  "last_event_id" text NOT NULL,
  "created_at" timestamptz NOT NULL DEFAULT clock_timestamp(),
  PRIMARY KEY ("id")
);
-- Create index "workos_user_syncs_last_event_id_desc_idx" to table: "workos_user_syncs"
CREATE INDEX "workos_user_syncs_last_event_id_desc_idx" ON "workos_user_syncs" ("last_event_id" DESC);
-- Create "organization_roles" table
CREATE TABLE "organization_roles" (
  "id" uuid NOT NULL DEFAULT generate_uuidv7(),
  "organization_id" text NOT NULL,
  "workos_slug" text NOT NULL,
  "workos_name" text NOT NULL,
  "workos_description" text NULL,
  "workos_created_at" timestamptz NOT NULL,
  "workos_updated_at" timestamptz NOT NULL,
  "workos_deleted_at" timestamptz NULL,
  "workos_deleted" boolean NOT NULL GENERATED ALWAYS AS (workos_deleted_at IS NOT NULL) STORED,
  "workos_last_event_id" text NULL,
  "created_at" timestamptz NOT NULL DEFAULT clock_timestamp(),
  "updated_at" timestamptz NOT NULL DEFAULT clock_timestamp(),
  "deleted_at" timestamptz NULL,
  "deleted" boolean NOT NULL GENERATED ALWAYS AS (deleted_at IS NOT NULL) STORED,
  PRIMARY KEY ("id"),
  CONSTRAINT "organization_roles_organization_id_fkey" FOREIGN KEY ("organization_id") REFERENCES "organization_metadata" ("id") ON UPDATE NO ACTION ON DELETE CASCADE
);
-- Create index "organization_roles_organization_id_workos_slug_key" to table: "organization_roles"
CREATE UNIQUE INDEX "organization_roles_organization_id_workos_slug_key" ON "organization_roles" ("organization_id", "workos_slug") WHERE (deleted IS FALSE);
