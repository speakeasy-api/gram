CREATE TABLE IF NOT EXISTS trace_summaries (
    -- Key cols
    trace_id FixedString(32),
    gram_project_id UUID,

    -- Filter cols. Plain vailes, filterable using WHERE
    gram_deployment_id SimpleAggregateFunction(any, Nullable(UUID)),
    gram_function_id SimpleAggregateFunction(any, Nullable(UUID)),
    gram_urn SimpleAggregateFunction(any, String),
    tool_name SimpleAggregateFunction(any, String),
    tool_source SimpleAggregateFunction(any, String),
    event_source SimpleAggregateFunction(any, String),
    user_email SimpleAggregateFunction(any, String),
    hook_source SimpleAggregateFunction(any, String),
    skill_name SimpleAggregateFunction(any, String),
    -- Tool-usage classification + identity cols, used to serve the unified Tool
    -- Logs page (hosted MCP / shadow MCP / skill / local target classification and
    -- multi-identity user resolution) directly from this MV instead of scanning raw
    -- telemetry_logs. toolset_slug/external_user_id/user_id are materialized columns
    -- on telemetry_logs; mcp_match/mcp_server_url are read from attributes.
    --
    -- These attributes are only present on a subset of a trace's rows (e.g. Cursor
    -- MCP traces carry gram.mcp.server_url/match only on the before/after MCP
    -- execution rows, while sibling rows in the same trace carry empty strings).
    -- Using any() lets ClickHouse persist or merge an empty string from a sibling
    -- row/part instead of the populated value, which would randomly misclassify a
    -- trace as shadow/local or drop its user identity. max() ensures empty strings
    -- ("") always lose to non-empty values during part merges, mirroring the
    -- block_reason treatment below.
    toolset_slug SimpleAggregateFunction(max, String),
    external_user_id SimpleAggregateFunction(max, String),
    user_id SimpleAggregateFunction(max, String),
    mcp_match SimpleAggregateFunction(max, String),
    mcp_server_url SimpleAggregateFunction(max, String),

    -- Aggregates
    start_time_unix_nano SimpleAggregateFunction(min, Int64),
    log_count SimpleAggregateFunction(sum, UInt64),

    http_status_code AggregateFunction(anyIf, Nullable(Int32), UInt8),

    -- Trace status signals (0 = false, 1 = true). Using max() ensures once we
    -- see a 1, it stays 1 across merges. Status is derived at query time as:
    -- has_error → failure, has_result → success, otherwise pending. This keeps
    -- the MV agnostic to specific hook event names: any future integration canhas_error
    -- signal failure by setting the gram.hook.error attribute and success by
    -- setting gen_ai.tool.call.result.
    has_result SimpleAggregateFunction(max, UInt8),
    has_error SimpleAggregateFunction(max, UInt8),
    -- Set when the Gram hook denied the tool call (e.g. shadow-MCP guard).
    -- Once we see a 1, it stays 1 across merges. Status is derived at query
    -- time as: has_block → blocked, has_error → failure, has_result → success,
    -- otherwise pending.
    has_block SimpleAggregateFunction(max, UInt8),
    -- The block reason surfaced in dashboards next to the blocked status.
    -- Uses max() so empty strings ("") always lose to non-empty reasons during
    -- part merges; any() is non-deterministic and can pick "" from a sibling
    -- row (e.g. the original PreToolUse log) instead of the row that actually
    -- carried the denial reason.
    block_reason SimpleAggregateFunction(max, String),
    -- AI account classification ('team' | 'personal'), materialized on
    -- telemetry_logs from attributes.gram.account_type. Only a subset of a
    -- trace's rows carry it (e.g. usage/tool rows), so max() lets a non-empty
    -- value win over empty siblings across part merges, mirroring user_id.
    -- Kept last so an ALTER ADD COLUMN (which appends) stays positionally aligned
    -- with this schema and the MV's projection.
    account_type SimpleAggregateFunction(max, String),
    -- AI provider for the account ('anthropic' | 'openai' | 'cursor'),
    -- materialized on telemetry_logs from attributes.gram.provider. Carried (not
    -- a sort key) — a trace is a single session = single account, so provider is
    -- constant within a trace_id; max() keeps a non-empty value over empties.
    provider SimpleAggregateFunction(max, String)
) ENGINE = AggregatingMergeTree
ORDER BY (gram_project_id, trace_id)
TTL fromUnixTimestamp64Nano(start_time_unix_nano) + INTERVAL 90 DAY
SETTINGS index_granularity = 8192
COMMENT 'Pre-aggregated trace summaries for fast trace-level queries without needing to scan all logs';

CREATE MATERIALIZED VIEW IF NOT EXISTS trace_summaries_mv TO trace_summaries AS
SELECT
    trace_id,
    gram_project_id,
    any(gram_deployment_id) AS gram_deployment_id,
    any(gram_function_id) AS gram_function_id,
    any(gram_urn) AS gram_urn,
    any(tool_name) AS tool_name,
    any(tool_source) AS tool_source,
    any(event_source) AS event_source,
    any(user_email) AS user_email,
    any(hook_source) AS hook_source,
    any(skill_name) AS skill_name,
    anyIf(toolset_slug, toolset_slug != '') AS toolset_slug,
    anyIf(external_user_id, external_user_id != '') AS external_user_id,
    anyIf(user_id, user_id != '') AS user_id,
    anyIf(toString(attributes.gram.mcp.match), toString(attributes.gram.mcp.match) != '') AS mcp_match,
    anyIf(toString(attributes.gram.mcp.server_url), toString(attributes.gram.mcp.server_url) != '') AS mcp_server_url,
    min(time_unix_nano) AS start_time_unix_nano,
    toUInt64(count(*)) AS log_count,
    anyIfState(
        toInt32OrNull(toString(attributes.http.response.status_code)),
        toString(attributes.http.response.status_code) != ''
    ) AS http_status_code,
    max(if(toString(attributes.gen_ai.tool.call.result) != '', 1, 0)) AS has_result,
    max(if(toString(attributes.gram.hook.error) != '', 1, 0)) AS has_error,
    max(if(toString(attributes.gram.hook.block_reason) != '', 1, 0)) AS has_block,
    anyIf(toString(attributes.gram.hook.block_reason), toString(attributes.gram.hook.block_reason) != '') AS block_reason,
    anyIf(account_type, account_type != '') AS account_type,
    anyIf(provider, provider != '') AS provider
FROM telemetry_logs
WHERE trace_id IS NOT NULL AND trace_id != '' AND NOT startsWith(telemetry_logs.gram_urn, 'urn:uuid:')
GROUP BY trace_id, gram_project_id;

