package plugins

import "strings"

// renderCursorHookScriptPowerShell produces hook.ps1, the Windows-native
// counterpart to the bash Cursor hook (renderHookScript(cfg, "cursor")).
//
// Cursor runs a hook's `command` through PowerShell on Windows, where `bash` is
// not reliably available — so the device agent rewrites the bundle's hooks.json
// to invoke this script there (the bundle's single `command` field can't carry a
// per-OS variant; see Cursor's local-hook schema). This script is a faithful,
// self-contained port: identity enrichment is inlined (the bash split into
// identity.sh/http.sh exists for cross-platform reuse we don't need here, and
// the Cursor flow never uses http.sh), so Windows ships exactly one extra file.
//
// Behavioral parity with the bash hook:
//   - read the Cursor event JSON from stdin, best-effort stamp user_email via
//     `<device-agent> identity` (default speakeasyd) with a short timeout,
//   - POST it to {server_url}/rpc/hooks.cursor with the Gram-Key (+ Gram-Project)
//     and X-Gram-Hook-Hostname headers,
//   - on 2xx relay the server's decision body verbatim and exit 0,
//   - on any other status, redirect, timeout, or network failure, fail CLOSED
//     by emitting a {"permission":"deny",...} body and exit 0 — so an outage
//     can't silently bypass a blocking policy.
//
// Targets Windows PowerShell 5.1 (the always-present `powershell`), so it sticks
// to APIs available there: Invoke-WebRequest -UseBasicParsing, ConvertTo/From-Json,
// [ordered], and System.Diagnostics.Process for the bounded identity probe.
func renderCursorHookScriptPowerShell(cfg GenerateConfig) []byte {
	keyPrefix := cfg.HooksAPIKey
	if len(keyPrefix) > 12 {
		keyPrefix = keyPrefix[:12]
	}

	projectHeaderLine := ""
	if cfg.ProjectSlug != "" {
		projectHeaderLine = "$headers['Gram-Project'] = '" + psSingleQuote(cfg.ProjectSlug) + "'"
	}

	// Token replacement (not fmt.Appendf) because the email regex contains a
	// literal % — doubling every % across a PowerShell script is error-prone.
	script := powerShellCursorHookTemplate
	script = strings.ReplaceAll(script, "__KEY_PREFIX__", keyPrefix)
	script = strings.ReplaceAll(script, "__SERVER_URL__", psSingleQuote(cfg.ServerURL))
	script = strings.ReplaceAll(script, "__GRAM_KEY__", psSingleQuote(cfg.HooksAPIKey))
	script = strings.ReplaceAll(script, "__GRAM_PROJECT_HEADER__", projectHeaderLine)
	return []byte(script)
}

// psSingleQuote escapes a value for embedding inside a PowerShell single-quoted
// string literal, where the only metacharacter is the single quote itself
// (doubled to escape). Keys/URLs/slugs won't normally contain one, but a
// generated secret could, and an unescaped quote would break the script.
func psSingleQuote(v string) string {
	return strings.ReplaceAll(v, "'", "''")
}

const powerShellCursorHookTemplate = `#!/usr/bin/env pwsh
# Generated by Speakeasy. Do not edit - overwritten on every publish.
# Key prefix: __KEY_PREFIX__ (correlate with the dashboard's API Keys page).
#
# Windows (PowerShell) port of the Cursor hook. Cursor reads the allow/deny
# decision from the JSON body on stdout; the server always responds 200 with
# {permission, user_message, agent_message}. When the server is unreachable or
# returns an error there is no decision to relay, so we fail CLOSED - emit a
# deny - rather than let an outage silently bypass blocking policies.
# Set GRAM_HOOKS_DEBUG=1 for stderr diagnostics.

$ErrorActionPreference = 'Stop'

$serverUrl = if ($env:GRAM_HOOKS_SERVER_URL) { $env:GRAM_HOOKS_SERVER_URL } else { '__SERVER_URL__' }

function Write-GramDebug([string]$message) {
  if ($env:GRAM_HOOKS_DEBUG) { [Console]::Error.WriteLine("gram-hooks(cursor): $message") }
}

# Best-effort: resolve the local user's email via a device agent. Any failure
# returns $null so the hook is never blocked on identity resolution.
function Get-GramUserEmail {
  $commands = if ($env:GRAM_DEVICE_AGENT_COMMANDS) { $env:GRAM_DEVICE_AGENT_COMMANDS } else { 'speakeasyd' }
  $timeoutMs = 1500
  if ($env:GRAM_DEVICE_AGENT_TIMEOUT_TENTHS) {
    $parsed = 0
    if ([int]::TryParse($env:GRAM_DEVICE_AGENT_TIMEOUT_TENTHS, [ref]$parsed) -and $parsed -gt 0) { $timeoutMs = $parsed * 100 }
  }
  $emailPattern = '[A-Za-z0-9._%+\-]+@[A-Za-z0-9.\-]+\.[A-Za-z]{2,}'
  foreach ($rawCommand in ($commands -split ',')) {
    $command = $rawCommand.Trim()
    if (-not $command) { continue }
    if (-not (Get-Command $command -ErrorAction SilentlyContinue)) {
      Write-GramDebug "device agent '$command' not found on PATH; trying next"
      continue
    }
    $output = $null
    try {
      $startInfo = New-Object System.Diagnostics.ProcessStartInfo
      $startInfo.FileName = $command
      $startInfo.Arguments = 'identity'
      $startInfo.RedirectStandardOutput = $true
      $startInfo.RedirectStandardError = $true
      $startInfo.UseShellExecute = $false
      $startInfo.CreateNoWindow = $true
      $process = [System.Diagnostics.Process]::Start($startInfo)
      if (-not $process.WaitForExit($timeoutMs)) {
        try { $process.Kill() } catch {}
        Write-GramDebug "device agent '$command identity' timed out after ${timeoutMs}ms; trying next"
        continue
      }
      $output = $process.StandardOutput.ReadToEnd()
    } catch {
      Write-GramDebug "device agent '$command identity' failed: $($_.Exception.Message); trying next"
      continue
    }
    if ($output -match "(?m)^\s*($emailPattern)\s*$") { return $Matches[1] }
    # Single-quoted literal (so the double quotes need no escaping) concatenated
    # with the pattern var, which a single-quoted string would not interpolate.
    $jsonEmailPattern = '"(?:email|user_email|userEmail|mail|preferred_username)"\s*:\s*"(' + $emailPattern + ')"'
    if ($output -match $jsonEmailPattern) { return $Matches[1] }
    Write-GramDebug "device agent '$command identity' returned no parseable email; trying next"
  }
  return $null
}

# Stamp user_email onto the payload. Best-effort: returns the payload unchanged
# on any parse/encode failure so a malformed event is never blocked here.
function Add-GramUserEmail([string]$payload) {
  $email = $null
  try { $email = Get-GramUserEmail } catch { $email = $null }
  if (-not $email) { return $payload }
  try {
    $event = $payload | ConvertFrom-Json -ErrorAction Stop
    if ($event -is [pscustomobject]) {
      $event | Add-Member -NotePropertyName 'user_email' -NotePropertyValue $email -Force
      return ($event | ConvertTo-Json -Depth 100 -Compress)
    }
  } catch {
    Write-GramDebug "could not enrich payload with user_email: $($_.Exception.Message)"
  }
  return $payload
}

function Write-GramDeny([string]$reason) {
  $deny = [ordered]@{ permission = 'deny'; user_message = $reason; agent_message = $reason }
  [Console]::Out.Write(($deny | ConvertTo-Json -Compress))
}

$payload = [Console]::In.ReadToEnd()
$payload = Add-GramUserEmail $payload

$headers = @{ 'Gram-Key' = '__GRAM_KEY__' }
__GRAM_PROJECT_HEADER__
$hookHostname = [System.Environment]::MachineName
if ($hookHostname) { $headers['X-Gram-Hook-Hostname'] = $hookHostname }

$body = [System.Text.Encoding]::UTF8.GetBytes($payload)
try {
  # -MaximumRedirection 0 mirrors the bash hook (plain curl follows nothing): a
  # 3xx carries no decision, so it must fall through to the fail-closed branch.
  $response = Invoke-WebRequest -Uri "$serverUrl/rpc/hooks.cursor" -Method Post -Headers $headers -ContentType 'application/json' -Body $body -TimeoutSec 10 -MaximumRedirection 0 -UseBasicParsing
  # 2xx: relay the server's decision verbatim so Cursor can honor allow/deny.
  [Console]::Out.Write($response.Content)
  exit 0
} catch {
  $statusCode = $null
  $failedResponse = $_.Exception.Response
  if ($failedResponse -and $failedResponse.StatusCode) { $statusCode = [int]$failedResponse.StatusCode }
  if ($statusCode) {
    $reason = "Speakeasy hook returned HTTP $statusCode, so this action was blocked. Retry in a moment; if it persists, check the Gram service status."
  } else {
    $reason = "Speakeasy could not reach the Gram server at $serverUrl, so this action was blocked. Check your network connection or GRAM_HOOKS_SERVER_URL."
  }
  Write-GramDebug "request failed (status=$statusCode); failing closed."
  Write-GramDeny $reason
  exit 0
}
`
