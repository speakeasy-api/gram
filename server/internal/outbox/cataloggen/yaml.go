package cataloggen

import (
	"bytes"
	"encoding/json"
	"fmt"
	"os"
	"path/filepath"

	"gopkg.in/yaml.v3"

	"github.com/speakeasy-api/gram/server/internal/outbox"
)

const specFile = "catalog_gen.yaml"

// GenerateYAML returns the YAML content of the OpenAPI 3.1 webhook catalog for
// the given set of event registrations.
func GenerateYAML(regs []outbox.EventRegistration) ([]byte, error) {
	webhooks := make(map[string]any, len(regs))
	for _, ev := range regs {
		var schemaNode any
		if err := json.Unmarshal(ev.JSONSchema(), &schemaNode); err != nil {
			return nil, fmt.Errorf("unmarshal schema for event %q: %w", ev.EventType(), err)
		}
		post := map[string]any{
			"operationId": string(ev.EventType()),
			"description": ev.Description(),
			"requestBody": map[string]any{
				"required": true,
				"content": map[string]any{
					"application/json": map[string]any{
						"schema": schemaNode,
					},
				},
			},
		}
		if ev.FeatureFlag() != "" {
			post["x-svix-feature-flag"] = ev.FeatureFlag()
		}
		if ev.GroupName() != "" {
			post["x-svix-group-name"] = ev.GroupName()
		}
		webhooks[string(ev.EventType())] = map[string]any{"post": post}
	}

	doc := map[string]any{
		"openapi": "3.1.0",
		"info": map[string]any{
			"title":   "Speakeasy API Control Plane Webhooks",
			"version": "0.0.0",
		},
		"webhooks": webhooks,
	}

	bs, err := yaml.Marshal(doc)
	if err != nil {
		return nil, fmt.Errorf("marshal webhook spec: %w", err)
	}
	return append([]byte("# Code generated by gen-webhooks. DO NOT EDIT.\n"), bs...), nil
}

// WriteYAML generates catalog_gen.yaml and writes it to dir/catalog_gen.yaml.
func WriteYAML(dir string, regs []outbox.EventRegistration) error {
	content, err := GenerateYAML(regs)
	if err != nil {
		return err
	}
	if err := os.WriteFile(filepath.Join(dir, specFile), content, 0o600); err != nil {
		return fmt.Errorf("write %s: %w", specFile, err)
	}
	return nil
}

// CheckYAML returns an error if the existing catalog_gen.yaml does not match
// what GenerateYAML would produce.
func CheckYAML(dir string, regs []outbox.EventRegistration) error {
	generated, err := GenerateYAML(regs)
	if err != nil {
		return err
	}
	existing, err := os.ReadFile(filepath.Join(dir, specFile)) //nolint:gosec // path is constructed from a trusted package directory
	if err != nil {
		return fmt.Errorf("read %s: %w", specFile, err)
	}
	if !bytes.Equal(generated, existing) {
		return fmt.Errorf("%s is out of date; run 'mise gen:webhooks-server' to regenerate", specFile)
	}
	return nil
}
