// Package cataloggen generates server/internal/outbox/events/catalog_gen.go and
// server/internal/outbox/events/catalog_gen.yaml from the event definition files.
package cataloggen

import (
	"bytes"
	"fmt"
	"go/ast"
	"go/format"
	"go/parser"
	"go/token"
	"os"
	"path/filepath"
	"strings"
)

const catalogFile = "catalog_gen.go"

// Generate returns the formatted content of a fresh catalog_gen.go for the
// events directory at dir.
func Generate(dir string) ([]byte, error) {
	names, err := collectEventDefNames(dir)
	if err != nil {
		return nil, err
	}
	return renderCatalog(names)
}

// Write generates catalog.go and writes it to dir/catalog.go.
func Write(dir string) error {
	content, err := Generate(dir)
	if err != nil {
		return err
	}
	if err := os.WriteFile(filepath.Join(dir, catalogFile), content, 0o600); err != nil {
		return fmt.Errorf("write %s: %w", catalogFile, err)
	}
	return nil
}

// Check returns an error if the existing catalog.go does not match what
// Generate would produce. Use this in tests or CI to enforce that the file is
// kept in sync with the event definition files.
func Check(dir string) error {
	generated, err := Generate(dir)
	if err != nil {
		return err
	}
	existing, err := os.ReadFile(filepath.Join(dir, catalogFile)) // #nosec G304 - path is constructed from a trusted package directory
	if err != nil {
		return fmt.Errorf("read %s: %w", catalogFile, err)
	}
	if !bytes.Equal(generated, existing) {
		return fmt.Errorf("%s is out of date; run 'mise gen:webhooks-server' to regenerate", catalogFile)
	}
	return nil
}

// collectEventDefNames returns the names of all package-level variables
// initialised with outbox.NewEventDef[T](...) found in non-catalog, non-test
// Go files in dir, in file-alphabetical then declaration order.
func collectEventDefNames(dir string) ([]string, error) {
	entries, err := os.ReadDir(dir) // ReadDir returns entries sorted by name
	if err != nil {
		return nil, fmt.Errorf("read events dir: %w", err)
	}

	fset := token.NewFileSet()
	var names []string
	for _, entry := range entries {
		name := entry.Name()
		if entry.IsDir() || !strings.HasSuffix(name, ".go") {
			continue
		}
		if strings.HasSuffix(name, "_test.go") || name == catalogFile {
			continue
		}
		f, err := parser.ParseFile(fset, filepath.Join(dir, name), nil, 0)
		if err != nil {
			return nil, fmt.Errorf("parse %s: %w", name, err)
		}
		names = append(names, eventDefNamesInFile(f)...)
	}
	return names, nil
}

func eventDefNamesInFile(f *ast.File) []string {
	var names []string
	for _, decl := range f.Decls {
		gd, ok := decl.(*ast.GenDecl)
		if !ok || gd.Tok != token.VAR {
			continue
		}
		for _, spec := range gd.Specs {
			vs, ok := spec.(*ast.ValueSpec)
			if !ok {
				continue
			}
			for i, val := range vs.Values {
				if isNewEventDefCall(val) && i < len(vs.Names) {
					if n := vs.Names[i].Name; n != "_" {
						names = append(names, n)
					}
				}
			}
		}
	}
	return names
}

// isNewEventDefCall reports whether expr is a call to outbox.NewEventDef[T](...).
func isNewEventDefCall(expr ast.Expr) bool {
	call, ok := expr.(*ast.CallExpr)
	if !ok {
		return false
	}
	switch fun := call.Fun.(type) {
	case *ast.IndexExpr: // NewEventDef[T](...)
		sel, ok := fun.X.(*ast.SelectorExpr)
		return ok && sel.Sel.Name == "NewEventDef"
	case *ast.IndexListExpr: // NewEventDef[T, U, ...](...)
		sel, ok := fun.X.(*ast.SelectorExpr)
		return ok && sel.Sel.Name == "NewEventDef"
	case *ast.SelectorExpr: // non-generic call — included for completeness
		return fun.Sel.Name == "NewEventDef"
	default:
		return false
	}
}

func renderCatalog(names []string) ([]byte, error) {
	var entries strings.Builder
	for _, name := range names {
		entries.WriteString("\t")
		entries.WriteString(name)
		entries.WriteString(",\n")
	}

	src := `// Code generated by gen-webhooks. DO NOT EDIT.

package events

import "github.com/speakeasy-api/gram/server/internal/outbox"

// All is the complete list of registered webhook event types.
var All = []outbox.EventRegistration{
` + entries.String() + `}
`
	out, err := format.Source([]byte(src))
	if err != nil {
		return nil, fmt.Errorf("format catalog source: %w", err)
	}
	return out, nil
}
