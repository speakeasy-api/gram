package main

import (
	"bytes"
	"fmt"
	"go/format"
	"log"
	"os"
	"strconv"
)

type prompt struct {
	constName string
	path      string
}

func main() {
	prompts := []prompt{
		{constName: "elementsSystemPrompt", path: "../../../client/dashboard/src/elements/prompts/system.txt"},
		{constName: "elementsChartPrompt", path: "../../../client/dashboard/src/elements/prompts/chart.txt"},
		{constName: "elementsGenerativeUIPrompt", path: "../../../client/dashboard/src/elements/prompts/generative-ui.txt"},
	}

	var src bytes.Buffer
	src.WriteString("// Code generated by \"go generate\". DO NOT EDIT.\n\n")
	src.WriteString("package assistants\n\n")

	for _, item := range prompts {
		contents, err := os.ReadFile(item.path)
		if err != nil {
			log.Fatalf("read %s: %v", item.path, err)
		}
		fmt.Fprintf(&src, "const %s = %s\n\n", item.constName, strconv.Quote(string(contents)))
	}

	formatted, err := format.Source(src.Bytes())
	if err != nil {
		log.Fatalf("format source: %v", err)
	}
	if err := os.WriteFile("elements_prompts_gen.go", formatted, 0o644); err != nil {
		log.Fatalf("write output: %v", err)
	}
}
