package main

import (
	"fmt"
	"go/format"
	"log"
	"os"
	"regexp"
	"strings"
)

func main() {
	// CWD is server/internal/telemetry/repo/ when invoked via go generate.
	schema, err := os.ReadFile("../../../clickhouse/schema.sql")
	if err != nil {
		log.Fatalf("read schema: %v", err)
	}

	// Scope to the telemetry_logs CREATE TABLE block to avoid false matches in other tables.
	tableRe := regexp.MustCompile(`(?s)CREATE TABLE IF NOT EXISTS telemetry_logs\s*\((.+?)\)\s*ENGINE\s*=`)
	tableMatch := tableRe.FindSubmatch(schema)
	if tableMatch == nil {
		log.Fatal("telemetry_logs table not found in clickhouse/schema.sql")
	}
	block := tableMatch[1]

	// Match: col_name <Type> MATERIALIZED toString(attributes.<path>)
	// Excludes resource_attributes.* — different namespace, not routable via AttributeFilter.
	colRe := regexp.MustCompile(`(\w+)\s+\w+\s+MATERIALIZED\s+toString\(attributes\.([^)]+)\)`)
	matches := colRe.FindAllSubmatch(block, -1)

	if len(matches) == 0 {
		log.Fatal("no MATERIALIZED toString(attributes.*) columns found in telemetry_logs")
	}

	var entries []string
	for _, m := range matches {
		path := string(m[2])
		col := string(m[1])
		entries = append(entries, fmt.Sprintf("\t%q: %q,", path, col))
	}

	src := fmt.Sprintf(`// Code generated by "go generate". DO NOT EDIT.

package repo

// materializedColumns maps bare attribute paths (as used in AttributeFilter.Path)
// to their pre-computed materialized column names in telemetry_logs.
// Only attributes.* columns are included; resource_attributes.* columns
// (e.g. deployment_id) use a different namespace and are excluded.
var materializedColumns = map[string]string{
%s
}
`, strings.Join(entries, "\n"))

	out, err := format.Source([]byte(src))
	if err != nil {
		log.Fatalf("format source: %v", err)
	}

	if err := os.WriteFile("materialized_columns_gen.go", out, 0o600); err != nil {
		log.Fatalf("write output: %v", err)
	}

	log.Printf("generated materialized_columns_gen.go with %d column(s)", len(matches))
}
