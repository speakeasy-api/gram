package externalcredentials

import (
	. "goa.design/goa/v3/dsl"
)

// ExternalCredentialSummary holds the common, provider-independent fields that
// live on the external_credentials supertype. It is the item shape returned by
// the generic list endpoint and is embedded into each provider result type.
var ExternalCredentialSummary = Type("ExternalCredentialSummary", func() {
	Description("Provider-independent summary of an external credential.")

	Attribute("id", String, "The ID of the external credential.", func() {
		Format(FormatUUID)
	})
	Attribute("organization_id", String, "The organization that owns the credential.")
	Attribute("provider", String, "The cloud provider of the credential.", func() {
		Enum("aws_iam", "gcp_iam")
	})
	Attribute("name", String, "A human-readable name for the credential.")
	Attribute("created_at", String, func() {
		Description("When the credential was created.")
		Format(FormatDateTime)
	})
	Attribute("updated_at", String, func() {
		Description("When the credential was last updated.")
		Format(FormatDateTime)
	})

	Required("id", "organization_id", "provider", "name", "created_at", "updated_at")
})

// AwsIamCredential is the full detail of an AWS IAM external credential. The
// authentication approach is implied by which fields are set (see
// CreateAwsIamCredentialForm).
var AwsIamCredential = Type("AwsIamCredential", func() {
	Description("An AWS IAM external credential.")

	Extend(ExternalCredentialSummary)

	Attribute("assume_role_arn", String, "The customer IAM role ARN Gram assumes.")
	Attribute("external_id", String, "The Gram-generated ExternalId the customer must require in their role trust policy. Present when Gram assumes the role with an ExternalId.")
	Attribute("oidc_audience", String, "The OIDC audience. Present when Gram assumes the role with a web identity.")
	Attribute("oidc_subject", String, "Optional OIDC subject pin (web-identity approach).")
	Attribute("sts_region", String, "Optional STS region override.")
})

// GcpIamCredential is the full detail of a GCP IAM external credential.
var GcpIamCredential = Type("GcpIamCredential", func() {
	Description("A GCP IAM external credential.")

	Extend(ExternalCredentialSummary)

	Attribute("impersonate_service_account", String, "The service account Gram impersonates (impersonation approach, or the WIF hop).")
	Attribute("wif_pool_id", String, "Workload Identity Federation pool ID.")
	Attribute("wif_provider_id", String, "Workload Identity Federation provider ID.")
	Attribute("wif_project_number", String, "GCP project number backing the WIF pool.")
})

// CreateAwsIamCredentialForm is the input for creating an AWS IAM credential.
// The authentication approach is inferred from which fields are provided:
//   - assume_role_arn alone: Gram assumes the role with a generated ExternalId.
//   - assume_role_arn + oidc_audience: Gram assumes the role with a web identity.
//   - none of them: a KMS key-policy grant to Gram's own principal.
//
// The ExternalId is generated by Gram and never supplied by the caller.
var CreateAwsIamCredentialForm = Type("CreateAwsIamCredentialForm", func() {
	Attribute("name", String, "A human-readable name for the credential.")
	Attribute("assume_role_arn", String, "The customer IAM role ARN Gram assumes. Omit for a KMS key-policy grant.")
	Attribute("oidc_audience", String, "The OIDC audience. Provide (with assume_role_arn) to assume the role with a web identity.")
	Attribute("oidc_subject", String, "Optional OIDC subject pin; only valid alongside oidc_audience.")
	Attribute("sts_region", String, "Optional STS region override.")

	Required("name")
})

// CreateGcpIamCredentialForm is the input for creating a GCP IAM credential.
// The authentication approach is inferred from which fields are provided:
//   - wif_* triple set: Workload Identity Federation (impersonate_service_account
//     is an optional hop).
//   - impersonate_service_account only: direct impersonation.
//   - none: Gram's ambient attached identity.
var CreateGcpIamCredentialForm = Type("CreateGcpIamCredentialForm", func() {
	Attribute("name", String, "A human-readable name for the credential.")
	Attribute("impersonate_service_account", String, "The service account Gram impersonates. Set alone for direct impersonation, or as the hop alongside the wif_* fields.")
	Attribute("wif_pool_id", String, "Workload Identity Federation pool ID. Set together with the other wif_* fields.")
	Attribute("wif_provider_id", String, "Workload Identity Federation provider ID. Set together with the other wif_* fields.")
	Attribute("wif_project_number", String, "GCP project number backing the WIF pool. Set together with the other wif_* fields.")

	Required("name")
})

// ListExternalCredentialsResult wraps the generic, supertype-only list items.
var ListExternalCredentialsResult = Type("ListExternalCredentialsResult", func() {
	Attribute("credentials", ArrayOf(ExternalCredentialSummary), "The organization's external credentials.")
	Required("credentials")
})
