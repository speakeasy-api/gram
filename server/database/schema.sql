-- Create the chat_messages table to store individual messages in each chat
CREATE TABLE IF NOT EXISTS chat_messages (
  id uuid NOT NULL DEFAULT generate_uuidv7(),
  seq BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY, -- Guarantees chat messages are processed in order

  chat_id uuid NOT NULL,
  project_id uuid,
  role TEXT NOT NULL CHECK (role IN ('user', 'assistant', 'system', 'tool')),
  content TEXT NOT NULL,
  -- if message content is small enough then we'll store it straight in the
  -- database column for fast retrieval. If it's too large then the
  -- `content_raw` will be NULL. We'll always store messages in an asset store
  -- referenced by `content_asset_id` and `content_asset_url`.
  content_raw JSONB,
  content_asset_url TEXT, -- denormalization of the asset URL for faster access
  model TEXT,
  message_id TEXT,
  finish_reason TEXT,
  tool_calls JSONB,
  prompt_tokens BIGINT NOT NULL DEFAULT 0,
  completion_tokens BIGINT NOT NULL DEFAULT 0,
  total_tokens BIGINT NOT NULL DEFAULT 0,

  -- If there was an error storing the message content in the asset store,
  -- we can record the error message here for debugging.
  storage_error TEXT,

  user_id TEXT,
  external_user_id TEXT,
  external_message_id TEXT,
  origin TEXT,
  user_agent TEXT,
  ip_address TEXT,
  source TEXT, -- Elements, Playground, etc.

  tool_call_id TEXT,
  tool_urn TEXT,
  tool_outcome TEXT,
  tool_outcome_notes TEXT,

  -- Chained hash of (role, content, tool_call_id) linked to the parent message's
  -- hash. Used to detect whether an incoming completion request has diverged
  -- from the stored history (e.g., client-side conversation compaction).
  -- Nullable because existing rows have no hash; backfilled lazily during the
  -- next capture turn.
  content_hash BYTEA,

  -- Monotonic counter per chat. Bumped when the matcher detects divergence from
  -- the stored content chain (compaction or edit). Messages belonging to the
  -- same logical conversation view share a generation.
  generation INTEGER NOT NULL DEFAULT 0,

  created_at timestamptz NOT NULL DEFAULT clock_timestamp(),

  -- Set when all active risk policies have analyzed this message.
  -- NULL means unanalyzed (or analysis was reset). Best-effort: policy
  -- version rescans and backfills are coordinated outside this field.
  risk_analyzed_at timestamptz,

  CONSTRAINT chat_messages_pkey PRIMARY KEY (id),
  CONSTRAINT chat_messages_chat_id_fkey FOREIGN KEY (chat_id) REFERENCES chats(id) ON DELETE CASCADE,
  CONSTRAINT chat_messages_seq_key UNIQUE (seq)
);

